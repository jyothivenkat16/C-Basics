/* All arthimetic operations*/
#include<stdio.h>
void main()
{
	int a,b,s,r,u,o,k;
	a=11;
	b=21;
	s=a+b;
	r=a-b;
	u=a*b;
	o=a/b;
	k=a%b;
	
	printf("sum of %d and %d is %d\n",a,b,s);
	printf("Difference of %d and %d is %d\n",a,b,r);
	printf("product of %d and %d is %d\n",a,b,u);
	printf("quotient of %d and %d is %d\n",a,b,o);
	printf("remainder of %d and %d is %d\n",a,b,k);
}
